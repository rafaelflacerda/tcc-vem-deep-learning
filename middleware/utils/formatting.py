def print_header(text):
    print(f"\n{'='*20} {text} {'='*20}")

def print_subheader(text):
    print(f"\n{'-'*20} {text} {'-'*20}")

def print_success(text):
    print(f"✅ {text}")

def print_error(text):
    print(f"❌ {text}")

def print_warning(text):
    print(f"⚠️ {text}")

def print_info(text):
    print(f"ℹ️ {text}")

def print_debug(text):
    print(f"🐞 {text}")